-- 이메일 템플릿
CREATE TABLE EMAIL_TEMPLATE (
    TEMPLATE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TEMPLATE_CODE VARCHAR2(100) NOT NULL,
    TEMPLATE_NAME VARCHAR2(100) NOT NULL,
    SUBJECT_TEMPLATE VARCHAR2(300) NOT NULL,
    BODY_TEMPLATE CLOB NOT NULL,
    USE_YN CHAR(1) DEFAULT 'Y' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT UK_EMAIL_TEMPLATE_CODE UNIQUE (TEMPLATE_CODE),
    CONSTRAINT CK_EMAIL_TEMPLATE_USE_YN CHECK (USE_YN IN ('Y', 'N'))
);

-- 이메일 이력
CREATE TABLE EMAIL_HISTORY (
    HISTORY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TEMPLATE_ID NUMBER NULL,
    REQUEST_TYPE VARCHAR2(20) NOT NULL,
    TO_EMAIL VARCHAR2(200) NOT NULL,
    SUBJECT VARCHAR2(300) NOT NULL,
    BODY CLOB NOT NULL,
    SEND_STATUS VARCHAR2(20) NOT NULL,
    FAIL_REASON VARCHAR2(1000),
    SENT_AT TIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_EMAIL_HISTORY_TEMPLATE FOREIGN KEY (TEMPLATE_ID)
        REFERENCES EMAIL_TEMPLATE (TEMPLATE_ID),
    CONSTRAINT CK_EMAIL_HISTORY_REQUEST_TYPE CHECK (REQUEST_TYPE IN ('TEMPLATE', 'DIRECT')),
    CONSTRAINT CK_EMAIL_HISTORY_SEND_STATUS CHECK (SEND_STATUS IN ('READY', 'SUCCESS', 'FAIL'))
);

CREATE INDEX IDX_EMAIL_HISTORY_TEMPLATE_ID ON EMAIL_HISTORY (TEMPLATE_ID);
CREATE INDEX IDX_EMAIL_HISTORY_TO_EMAIL ON EMAIL_HISTORY (TO_EMAIL);
CREATE INDEX IDX_EMAIL_HISTORY_STATUS ON EMAIL_HISTORY (SEND_STATUS);
